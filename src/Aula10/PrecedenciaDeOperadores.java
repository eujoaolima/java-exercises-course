package Aula10;

public class PrecedenciaDeOperadores {
    public static void main(String[] args) {
        // Precedencia de Operadores

        // Nada mais é do que definir a ordem de execução utilizando parenteses e colchetes

        // Exemplo:

        int abc = 123;
        int def = 22;
        int ghi = 11;
        int resultado = (abc + def) - ghi;
        System.out.print(resultado);
    }
}
